package com.bcp.printer;

public class DetailPrinter {

    String date;
    String hourOpe;
    String noOpe;
    String amount;
    String symbol;
    String entryOrExit;
    String transEname;
    String inputMode;
    String symbolExchange;
    String amountExchange;

    public String getDate() {
        return date;
    }

    public void setDate(String date) {
        this.date = date;
    }

    public String getHourOpe() {
        return hourOpe;
    }

    public void setHourOpe(String hourOpe) {
        this.hourOpe = hourOpe;
    }

    public String getNoOpe() {
        return noOpe;
    }

    public void setNoOpe(String noOpe) {
        this.noOpe = noOpe;
    }

    public String getAmount() {
        return amount;
    }

    public void setAmount(String amount) {
        this.amount = amount;
    }

    public String getSymbol() {
        return symbol;
    }

    public void setSymbol(String symbol) {
        this.symbol = symbol;
    }

    public String getEntryOrExit() {
        return entryOrExit;
    }

    public void setEntryOrExit(String entryOrExit) {
        this.entryOrExit = entryOrExit;
    }

    public String getTransEname() {
        return transEname;
    }

    public void setTransEname(String transEname) {
        this.transEname = transEname;
    }

    public String getInputMode() {
        return inputMode;
    }

    public void setInputMode(String inputMode) {
        this.inputMode = inputMode;
    }

    public String getSymbolExchange() {
        return symbolExchange;
    }

    public void setSymbolExchange(String symbolExchange) {
        this.symbolExchange = symbolExchange;
    }

    public String getAmountExchange() {
        return amountExchange;
    }

    public void setAmountExchange(String amountExchange) {
        this.amountExchange = amountExchange;
    }
}
